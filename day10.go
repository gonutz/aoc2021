package main

import (
	"fmt"
	"sort"
	"strings"
)

func main() {
	opposite := map[rune]rune{'(': ')', '[': ']', '{': '}', '<': '>'}

	corruptScores := map[rune]int{')': 3, ']': 57, '}': 1197, '>': 25137}
	corruptScore := 0

	completionScores := map[rune]int{')': 1, ']': 2, '}': 3, '>': 4}
	var allCompletionScores []int

	for _, line := range strings.Split(input, "\n") {
		func() {
			var stack []rune
			push := func(r rune) {
				stack = append(stack, r)
			}
			top := func() rune {
				return stack[len(stack)-1]
			}
			pop := func() {
				stack = stack[:len(stack)-1]
			}
			for _, r := range line + "\n" {
				if r == '\n' {
					score := 0
					for len(stack) > 0 {
						score = score*5 + completionScores[top()]
						pop()
					}
					allCompletionScores = append(allCompletionScores, score)
					return
				} else if strings.ContainsRune("([{<", r) {
					push(opposite[r])
				} else if top() == r {
					pop()
				} else {
					corruptScore += corruptScores[r]
					return
				}
			}
			return
		}()
	}
	sort.Ints(allCompletionScores)

	fmt.Println("part 1)", corruptScore)
	fmt.Println("part 2)", allCompletionScores[len(allCompletionScores)/2])
}

