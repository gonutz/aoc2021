func getBits(count int) int {
	value := 0
	for i := 0; i < count; i++ {
		value = value*2 + bits[0]
		bits = bits[1:]
	}
	return value
}

func parsePacketVersion() int {
	version := getBits(3)
	typ := getBits(3)
	if typ == 4 {
		parseLiteral()
	} else {
		parseOperator()
	}
	return version
}

func parseLiteral() {
	n := getBits(5)
	for n >= 0x10 {
		n = getBits(5)
	}
}

func parseOperator() {
	if getBits(1) == 0 {
		getBits(15)
	} else {
		getBits(11)
	}
}

func evalPacket() int {
	getBits(3)
	typ := getBits(3)
	if typ == 4 {
		return evalLiteral()
	} else {
		return evalOperator(typ)
	}
}

func evalLiteral() int {
	num := 0
	for {
		n := getBits(5)
		num = (num << 4) + (n & 0xF)
		if n < 0x10 {
			break
		}
	}
	return num
}

func evalOperator(typ int) int {
	var packets []int
	if getBits(1) == 0 {
		bitCount := getBits(15)
		remaining := len(bits) - bitCount
		for len(bits) > remaining {
			packets = append(packets, evalPacket())
		}
	} else {
		packetCount := getBits(11)
		for i := 0; i < packetCount; i++ {
			packets = append(packets, evalPacket())
		}
	}

	switch typ {
	case 0:
		sum := 0
		for _, n := range packets {
			sum += n
		}
		return sum
	case 1:
		prod := 1
		for _, n := range packets {
			prod *= n
		}
		return prod
	case 2:
		min := packets[0]
		for _, n := range packets {
			if n < min {
				min = n
			}
		}
		return min
	case 3:
		max := packets[0]
		for _, n := range packets {
			if n > max {
				max = n
			}
		}
		return max
	case 5:
		if packets[0] > packets[1] {
			return 1
		}
		return 0
	case 6:
		if packets[0] < packets[1] {
			return 1
		}
		return 0
	case 7:
		if packets[0] == packets[1] {
			return 1
		}
		return 0
	default:
		panic("this was a literal")
	}
}

